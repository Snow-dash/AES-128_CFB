U8ZI mul(U8ZI a,U8ZI b)
{
	int i=0;
	U8ZI ans=0;
	if(a>b)
	{
		return mul(b,a);
	}
	if(a==0 || b==0) return 0;
	if(a==1) return b;
	if(a>2 && a%2==0) return mul(a/2,mul(2,b)); //if(a%2==0) return mul(mul(2,b),a/2);
	if(a==2)
	{
		if((b>>7)==0)
		{
			return b<<1;
		}
		else
		{
			return (b<<1) ^ (0x1b);
		}
	}
	else
	{
		/*
		0010 0010
		0+
		*/
		//a&0x1
		ans=mul((a)&(0x1),b);
		for(i=1;i<=7;i++)
		{
			//if( (a>>i)&(0x1) ==1)
			if((a>>i)==0) break;
			ans=ans ^ ( mul(mul((a>>i)&(0x1),(0x1<<i)),b));
		}
		//return(0);
		return ans;
	}
	//return 0;
}
void keysp()
{
	int i=0;
	for(i=0;i<44;i++)
	{
		if(i<=3)
		{
			W[i]=pin(K[0][i],K[1][i],K[2][i],K[3][i]);
		}
		else
		{
			if(i%4!=0)
			{
				W[i]=W[i-4] ^ W[i-1];
			}
			else
			{
				W[i]=W[i-4] ^ T(W[i-1],i/4);
			}
		}
	}
}
U32ZI T(U32ZI w,int j)
{
	//0xfff;
	U32ZI Rcon[11]={0,0x01000000ul,0x02000000ul,0x04000000ul,0x08000000ul,0x10000000ul,0x20000000ul,0x40000000ul,0x80000000ul,0x1b000000ul,0x36000000ul};
	w=le(w,8);
	w=pin( S[ (U8ZI)(w>>24) ] , S[ (U8ZI)((w>>16)&0xff) ] , S[ (U8ZI)((w>>8)&0xff) ] , S[ (U8ZI)(w&0xff) ]);
	w=w ^ Rcon[j];
	return w;
}


U32ZI le(U32ZI x,U32ZI a)
{
	return x<<a | x>>(32-a);
}
U32ZI ri(U32ZI x,U32ZI a)
{
	return x>>a | x<<(32-a);
}
U32ZI pin(U8ZI a,U8ZI b,U8ZI c,U8ZI d)
{
	return ((U32ZI)a)<<24 | ((U32ZI)b)<<16 | ((U32ZI)c)<<8 | ((U32ZI)d);
}
