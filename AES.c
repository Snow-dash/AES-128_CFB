void keymix(int i1,int r)
{
	U32ZI temp;
	temp=pin(ST[0][i1],ST[1][i1],ST[2][i1],ST[3][i1]) ^ W[4*r+i1];
	ST[0][i1]=(U8ZI)(temp>>24);
	ST[1][i1]=((U8ZI)(temp>>16)) & 0xff;
	ST[2][i1]=((U8ZI)(temp>>8)) & 0xff;
	ST[3][i1]=((U8ZI)(temp>>0)) & 0xff;
}
